class Group {
  String title;
  String skill;
  String size;
  String location;
  bool isCalling;
  Group(String title, String skill, String size, String location,
      bool isCalling) {
    this.title = title;
    this.skill = skill;
    this.size = size;
    this.location = location;
    this.isCalling = isCalling;
  }
}
