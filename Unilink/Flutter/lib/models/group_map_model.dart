class GroupMap {
  String partyId;
  String title;
  String skill;
  String size;
  String location;
  String description;
  GroupMap(String partyId, String title, String skill, String size,
      String location, String description) {
    this.partyId = partyId;
    this.title = title;
    this.skill = skill;
    this.size = size;
    this.location = location;
    this.description = description;
  }
}
